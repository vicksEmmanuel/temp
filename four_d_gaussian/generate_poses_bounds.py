def _run_cut3r_inference(model, img_paths, size=512, device="cuda"):
    """Run CUT3R inference on a list of image paths.
    
    Returns:
        pts3d_self: (B, H, W, 3) per-view 3D points in self-view coords
        pts3d_world: (B, H, W, 3) per-view 3D points in world coords  
        c2w_poses: (B, 4, 4) camera-to-world poses
        focals: (B,) estimated focal lengths
        pp: (B, 2) principal points
        conf_self: (B, H, W) confidence maps
        colors: (B, H, W, 3) image colors [0,1]
    """
    import torch
    from src.dust3r.utils.image import load_images_512
    from src.dust3r.inference import inference
    from src.dust3r.utils.camera import pose_encoding_to_camera
    from src.dust3r.post_process import estimate_focal_knowing_depth
    from src.dust3r.utils.geometry import geotrf
    
    # Prepare input views — use load_images_512 to force uniform size
    images = load_images_512(img_paths, size=size)
    views = []
    for i in range(len(images)):
        view = {
            "img": images[i]["img"],
            "ray_map": torch.full(
                (images[i]["img"].shape[0], 6,
                 images[i]["img"].shape[-2], images[i]["img"].shape[-1]),
                torch.nan,
            ),
            "true_shape": torch.from_numpy(images[i]["true_shape"]),
            "idx": i,
            "instance": str(i),
            "camera_pose": torch.from_numpy(np.eye(4, dtype=np.float32)).unsqueeze(0),
            "img_mask": torch.tensor(True).unsqueeze(0),
            "ray_mask": torch.tensor(False).unsqueeze(0),
            "update": torch.tensor(True).unsqueeze(0),
            "reset": torch.tensor(False).unsqueeze(0),
        }
        views.append(view)
    
    # Run inference
    print(f"Running CUT3R inference on {len(views)} views...")
    outputs, state_args = inference(views, model, device)
    
    # Extract results
    pts3d_self_ls = [output["pts3d_in_self_view"].cpu() for output in outputs["pred"]]
    conf_self_ls = [output["conf_self"].cpu() for output in outputs["pred"]]
    pts3d_self = torch.cat(pts3d_self_ls, 0)  # (B, H, W, 3)
    conf_self = torch.cat(conf_self_ls, 0)  # (B, H, W)
    
    # Recover camera poses (c2w in OpenCV convention)
    pr_poses = [
        pose_encoding_to_camera(pred["camera_pose"].clone()).cpu()
        for pred in outputs["pred"]
    ]
    c2w_poses = torch.cat(pr_poses, 0)  # (B, 4, 4)
    
    # Transform self-view points to world coords using c2w
    pts3d_world_ls = []
    for pose, pself in zip(pr_poses, pts3d_self_ls):
        pts3d_world_ls.append(geotrf(pose, pself.unsqueeze(0) if pself.dim() == 3 else pself))
    pts3d_world = torch.cat(pts3d_world_ls, 0)  # (B, H, W, 3)
    
    # Estimate focal length
    B, H, W, _ = pts3d_self.shape
    pp = torch.tensor([W // 2, H // 2], device=pts3d_self.device).float().repeat(B, 1)
    focals = estimate_focal_knowing_depth(pts3d_self, pp, focal_mode="weiszfeld")
    
    # Extract colors
    colors = torch.cat([
        0.5 * (output["img"].permute(0, 2, 3, 1).cpu() + 1.0)
        for output in outputs["views"]
    ], 0)  # (B, H, W, 3)
    
    return pts3d_self, pts3d_world, c2w_poses, focals, pp, conf_self, colors


def _subsample_points_by_confidence(pts3d, colors, conf, max_points=50000, conf_percentile=50):
    """Subsample dense point cloud by confidence, then random if still too many.
    
    Args:
        pts3d: (H, W, 3) or (N, 3) points
        colors: (H, W, 3) or (N, 3) RGB [0,1]
        conf: (H, W) or (N,) confidence
        max_points: maximum number of points to return
        conf_percentile: percentile threshold for confidence filtering
    
    Returns:
        xyz: (M, 3) subsampled points
        rgb: (M, 3) subsampled colors [0, 255]
    """
    import torch
    if pts3d.dim() == 3:
        pts3d = pts3d.reshape(-1, 3)
        colors = colors.reshape(-1, 3)
        conf = conf.reshape(-1)
    
    # Remove NaN/inf points
    valid = torch.isfinite(pts3d).all(dim=-1) & torch.isfinite(conf) & (conf > 0)
    pts3d = pts3d[valid]
    colors = colors[valid]
    conf = conf[valid]
    
    if len(pts3d) == 0:
        return np.zeros((0, 3)), np.zeros((0, 3), dtype=np.uint8)
    
    # Confidence filter: keep points above percentile threshold
    conf_threshold = torch.quantile(conf, conf_percentile / 100.0)
    mask = conf >= conf_threshold
    pts3d = pts3d[mask]
    colors = colors[mask]
    conf = conf[mask]
    
    # Random subsample if still too many
    if len(pts3d) > max_points:
        # Weight by confidence for importance sampling
        weights = conf / conf.sum()
        indices = torch.multinomial(weights, max_points, replacement=False)
        pts3d = pts3d[indices]
        colors = colors[indices]
    
    xyz = pts3d.numpy()
    rgb = (colors.numpy() * 255).clip(0, 255).astype(np.uint8)
    return xyz, rgb


def generate_poses_cut3r(video_folder, output_name="poses_bounds.npy",
                         model_path=None, device="cuda", size=512,
                         max_points_per_view=4000):

    """Generate poses_bounds.npy using CUT3R (dense regression-based 3D reconstruction).
    
    Extracts one frame per video, runs CUT3R to get camera poses plus dense 3D points,
    then produces poses_bounds.npy in LLFF format and writes COLMAP-compatible sparse model.
    
    Args:
        video_folder: Path to folder containing cam*.mp4 files
        output_name: Output filename for poses_bounds
        model_path: Path to CUT3R checkpoint (.pth)
        device: torch device
        size: CUT3R input size (512 for the DPT model)
        max_points_per_view: Maximum points per view for initialization
    """
    import torch
    video_folder = Path(video_folder)
    video_files = sorted(video_folder.glob("cam*.mp4"))
    if not video_files:
        print(f"No video files found in {video_folder}")
        return
    
    n_cams = len(video_files)
    print(f"Found {n_cams} videos in {video_folder}")
    
    if model_path is None:
        model_path = CUT3R_DEFAULT_CKPT
    model_path = Path(model_path)
    
    # Step 1: Extract 1 frame per video
    target_w, target_h = get_video_dims(video_files[0])
    print(f"Video resolution: {target_w}x{target_h}")
    
    images_dir = video_folder / "images_cut3r_tmp"
    images_dir.mkdir(exist_ok=True)
    
    img_paths = []
    print("Extracting frames for CUT3R...")
    for video in video_files:
        output_path = images_dir / f"{video.stem}.jpg"
        if not output_path.exists():
            subprocess.run([
                "ffmpeg", "-i", str(video),
                "-frames:v", "1",
                "-q:v", "2",
                "-loglevel", "error",
                str(output_path)
            ], check=True)
        img_paths.append(str(output_path))
    
    # Step 1b: Resize all frames to the same resolution (CUT3R requires uniform sizes for batching)
    # Use 1920x1080 (standard 16:9) which gives 512x288 = 32x18 patches after CUT3R rescaling
    # Avoids odd patch counts that cause CUDA indexing errors in RoPE
    import cv2
    target_res = (1920, 1080)  # w, h
    print(f"Normalizing all frames to {target_res[0]}x{target_res[1]} for CUT3R compatibility")
    for p in img_paths:
        img = cv2.imread(p)
        if img.shape[:2] != (target_res[1], target_res[0]):
            img_resized = cv2.resize(img, target_res, interpolation=cv2.INTER_LANCZOS4)
            cv2.imwrite(p, img_resized)
            print(f"  Resized {Path(p).name}: {img.shape[1]}x{img.shape[0]} -> {target_res[0]}x{target_res[1]}")
    
    # Step 2: Load CUT3R model and run inference
    model = _load_cut3r_model(model_path, device)
    pts3d_self, pts3d_world, c2w_poses, focals, pp, conf_self, colors = \
        _run_cut3r_inference(model, img_paths, size=size, device=device)
    
    B, H, W, _ = pts3d_self.shape
    print(f"CUT3R output: {B} views, {H}x{W} resolution")
    
    # Step 3: Build poses_bounds.npy in LLFF format
    # LLFF format: each row = [R(3x3) flattened | t(3) | hwf(3) | near | far] = 17 values
    # The R,t are in LLFF convention (DRB): rows = [-up, right, -forward]
    poses_bounds_rows = []
    
    for i in range(B):
        c2w = c2w_poses[i].numpy()  # 4x4, OpenCV convention
        R_cv = c2w[:3, :3]  # OpenCV rotation
        t_cv = c2w[:3, 3]   # OpenCV translation
        
        # Convert OpenCV c2w rotation to LLFF convention
        # LLFF convention: row0 = -up (OpenCV row1), row1 = right (OpenCV row0), row2 = -forward (-OpenCV row2)
        R_llff = M_LLFF @ R_cv
        t_llff = M_LLFF @ t_cv
        
        # Orthogonalize to ensure proper rotation matrix
        R_llff = orthogonalize(R_llff)
        
        # Focal length: CUT3R estimates focal for the resized image (HxW)
        # Scale to original image dimensions
        focal_cut3r = focals[i].item()
        scale_w = target_w / W
        scale_h = target_h / H
        focal_orig = focal_cut3r * max(scale_w, scale_h)
        
        # Compute near/far from depth (z-values in self-view)
        depths = pts3d_self[i, :, :, 2]  # (H, W)
        valid_depths = depths[torch.isfinite(depths) & (depths > 0)]
        if len(valid_depths) > 0:
            near = torch.quantile(valid_depths, 0.02).item()
            far = torch.quantile(valid_depths, 0.98).item()
        else:
            near = 0.1
            far = 100.0
        # Safety margins
        near = max(near * 0.8, 0.01)
        far = far * 1.2
        
        # Pack as LLFF: [3x5 matrix flattened to 15] ++ [near, far]
        # 3x5 matrix = [R(3x3) | t(3x1) | hwf(3x1)]
        hwf = np.array([target_h, target_w, focal_orig])
        pose_3x5 = np.concatenate([R_llff, t_llff.reshape(3, 1), hwf.reshape(3, 1)], axis=1)
        row = np.concatenate([pose_3x5.flatten(), np.array([near, far])])
        poses_bounds_rows.append(row)
    
    poses_bounds = np.array(poses_bounds_rows, dtype=np.float64)
    
    # Save poses_bounds.npy
    pb_path = video_folder / output_name
    np.save(pb_path, poses_bounds)
    print(f"✅ CUT3R generated {output_name}: shape={poses_bounds.shape}")
    
    # Create COLMAP sparse structure
    colmap_dir = video_folder / "colmap_0"
    sparse_dir = colmap_dir / "sparse" / "0"
    manual_dir = colmap_dir / "manual"
    
    sparse_dir.mkdir(parents=True, exist_ok=True)
    manual_dir.mkdir(parents=True, exist_ok=True)


    
    # Build camera and image lists for COLMAP format
    colmap_cameras = []
    colmap_images = []
    all_points = {}
    point_id_counter = 1
    
    for i in range(B):
        cam_name = video_files[i].stem  # e.g., "cam00"
        c2w = c2w_poses[i].numpy()
        
        # Focal scaled to original resolution
        focal_cut3r = focals[i].item()
        scale_w = target_w / W
        scale_h = target_h / H
        fx = focal_cut3r * scale_w
        fy = focal_cut3r * scale_h
        cx = target_w / 2.0
        cy = target_h / 2.0
        
        colmap_cameras.append({
            "id": i + 1,
            "width": target_w,
            "height": target_h,
            "fx": fx,
            "fy": fy,
            "cx": cx,
            "cy": cy,
        })
        
        # Convert c2w to w2c for COLMAP
        w2c = np.linalg.inv(c2w)
        R_w2c = w2c[:3, :3]
        t_w2c = w2c[:3, 3]
        qvec = rotmat2qvec(R_w2c)
        
        colmap_images.append({
            "id": i + 1,
            "qvec": qvec,
            "tvec": t_w2c,
            "camera_id": i + 1,
            "name": f"{cam_name}.png",
        })
        
        # Subsample dense points from this view into world coordinates
        xyz, rgb = _subsample_points_by_confidence(
            pts3d_world[i], colors[i], conf_self[i],
            max_points=max_points_per_view
        )
        
        for j in range(len(xyz)):
            all_points[point_id_counter] = {
                "xyz": xyz[j],
                "rgb": rgb[j],
                "error": 0.0,
            }
            point_id_counter += 1
    
    print(f"Total initialization points: {len(all_points)}")
    
    # Write COLMAP binary files
    write_cameras_binary(colmap_cameras, sparse_dir / "cameras.bin")
    write_images_binary(colmap_images, sparse_dir / "images.bin")
    write_points3D_binary(all_points, sparse_dir / "points3D.bin")
