import struct
import collections

CameraModel = collections.namedtuple("CameraModel", ["model_id", "model_name", "num_params"])
CAMERA_MODEL_PINHOLE = CameraModel(model_id=1, model_name="PINHOLE", num_params=4)


def write_cameras_binary(cameras, path):
    """Write COLMAP cameras.bin file.
    
    cameras: list of dicts with keys: id, width, height, fx, fy, cx, cy
    """
    with open(path, "wb") as f:
        f.write(struct.pack("<Q", len(cameras)))
        for cam in cameras:
            camera_id = cam["id"]
            model_id = CAMERA_MODEL_PINHOLE.model_id
            width = int(cam["width"])
            height = int(cam["height"])
            params = [cam["fx"], cam["fy"], cam["cx"], cam["cy"]]
            f.write(struct.pack("<I", camera_id))
            f.write(struct.pack("<i", model_id))
            f.write(struct.pack("<Q", width))
            f.write(struct.pack("<Q", height))
            for p in params:
                f.write(struct.pack("<d", p))


def write_images_binary(images, path):
    """Write COLMAP images.bin file.
    
    images: list of dicts with keys: id, qvec(4), tvec(3), camera_id, name
    """
    with open(path, "wb") as f:
        f.write(struct.pack("<Q", len(images)))
        for img in images:
            image_id = img["id"]
            qvec = img["qvec"]
            tvec = img["tvec"]
            camera_id = img["camera_id"]
            name = img["name"]
            
            f.write(struct.pack("<I", image_id))
            for q in qvec:
                f.write(struct.pack("<d", q))
            for t in tvec:
                f.write(struct.pack("<d", t))
            f.write(struct.pack("<I", camera_id))
            # Write image name as null-terminated string
            for ch in name:
                f.write(struct.pack("<c", ch.encode("utf-8")))
            f.write(struct.pack("<c", b"\x00"))
            # Number of 2D points (we write 0 — no 2D-3D correspondences needed)
            f.write(struct.pack("<Q", 0))


def write_points3D_binary(points3D, path):
    """Write COLMAP points3D.bin file.
    
    points3D: dict of {point_id: {"xyz": (3,), "rgb": (3,), "error": float}}
    """
    import numpy as np
    num_points = len(points3D)
    with open(path, "wb") as f:
        f.write(struct.pack("<Q", num_points))
        
        # Prepare structured array for fast writing
        # POINT3D_ID (Q:8) | X, Y, Z (ddd:24) | R, G, B (BBB:3) | ERROR (d:8) | TRACK_LENGTH (Q:8)
        data = np.zeros(num_points, dtype=[
            ('id', '<Q'),
            ('xyz', '<d', 3),
            ('rgb', '<B', 3),
            ('error', '<d'),
            ('track_len', '<Q')
        ])
        
        # This is still a loop but it only happens once during pose generation
        # and dictionaries are inherently slow to iterate.
        # We can optimize if points3D was already a numpy array, but for now
        # let's just make the writing part fast.
        for i, (point_id, pt) in enumerate(points3D.items()):
            data[i]['id'] = point_id
            data[i]['xyz'] = pt["xyz"]
            data[i]['rgb'] = pt["rgb"]
            data[i]['error'] = pt.get("error", 0.0)
            data[i]['track_len'] = 0
            
        data.tofile(f)



